#!/usr/bin/env python3
"""Build CP INT8 TRT engine with entropy calibration.

Requires calibration data at /tmp/cp_calib/ (generated by tts_sherpa.py).
Run on Jetson host with containers stopped:
    docker stop $(docker ps -q)
    python3 /tmp/build_cp_int8.py
"""
import pycuda.driver as cuda
import pycuda.autoinit  # Must be before tensorrt
import tensorrt as trt
import numpy as np
import os, glob

ONNX_PATH = "/tmp/code_predictor.onnx"
CALIB_DIR = "/tmp/cp_calib"
ENGINE_PATH = "/tmp/cp_sherpa_int8.engine"


class CPCalibrator(trt.IInt8EntropyCalibrator2):
    def __init__(self, calib_dir, batch_size=1, max_samples=500):
        super().__init__()
        self.batch_size = batch_size
        self.current = 0
        self.cache_file = "/tmp/cp_int8_cache.bin"

        # Load calibration data
        ctx_files = sorted(glob.glob(f"{calib_dir}/ctx_*.npy"))[:max_samples]
        gs_files = sorted(glob.glob(f"{calib_dir}/gs_*.npy"))[:max_samples]
        self.n_samples = len(ctx_files)
        print(f"Calibrator: {self.n_samples} samples")

        # Use actual (non-padded) context lengths for calibration
        self.contexts = []
        self.gen_steps = []
        max_ctx_len = 17
        for cf, gf in zip(ctx_files, gs_files):
            ctx = np.load(cf)  # [1, ctx_len, 1024] - variable length
            gs = np.load(gf)   # [1]
            self.contexts.append(ctx.astype(np.float32))
            self.gen_steps.append(gs)

        # Allocate GPU memory at max size
        self.cuda = cuda
        ctx_bytes = 1 * max_ctx_len * 1024 * 4
        self.d_ctx = cuda.mem_alloc(ctx_bytes)
        self.d_gs = cuda.mem_alloc(8)

    def get_batch_size(self):
        return self.batch_size

    def get_batch(self, names, p_func=None):
        if self.current >= self.n_samples:
            return None
        ctx = np.ascontiguousarray(self.contexts[self.current])
        gs = np.ascontiguousarray(self.gen_steps[self.current])
        self.cuda.memcpy_htod(self.d_ctx, ctx)
        self.cuda.memcpy_htod(self.d_gs, gs)
        self.current += 1
        if self.current % 100 == 0:
            print(f"  Calibrated {self.current}/{self.n_samples}")
        return [int(self.d_ctx), int(self.d_gs)]

    def read_calibration_cache(self):
        if os.path.exists(self.cache_file):
            with open(self.cache_file, "rb") as f:
                return f.read()
        return None

    def write_calibration_cache(self, cache):
        with open(self.cache_file, "wb") as f:
            f.write(cache)
        print(f"Calibration cache saved ({len(cache)} bytes)")


def main():
    logger = trt.Logger(trt.Logger.INFO)
    builder = trt.Builder(logger)
    network = builder.create_network(1 << int(trt.NetworkDefinitionCreationFlag.EXPLICIT_BATCH))
    parser = trt.OnnxParser(network, logger)

    print(f"Parsing {ONNX_PATH}...")
    with open(ONNX_PATH, "rb") as f:
        if not parser.parse(f.read()):
            for i in range(parser.num_errors):
                print(f"  Error: {parser.get_error(i)}")
            raise RuntimeError("Parse failed")

    print(f"Network: {network.num_layers} layers")

    # Configure
    config = builder.create_builder_config()
    config.set_memory_pool_limit(trt.MemoryPoolType.WORKSPACE, 1024 * 1024 * 1024)
    config.set_flag(trt.BuilderFlag.INT8)
    config.set_flag(trt.BuilderFlag.FP16)  # Allow FP16 fallback for unsupported INT8 ops

    # Dynamic shapes — calibrate with padded max length
    profile = builder.create_optimization_profile()
    profile.set_shape("context", (1, 2, 1024), (1, 9, 1024), (1, 17, 1024))
    profile.set_shape("gen_step", (1,), (1,), (1,))
    config.add_optimization_profile(profile)

    # INT8 calibrator
    calibrator = CPCalibrator(CALIB_DIR, max_samples=500)
    config.int8_calibrator = calibrator

    # Let TRT decide precision per-layer with INT8+FP16 flexibility
    # Don't force individual layers — TRT auto-selects best precision
    print("INT8+FP16 mixed precision (TRT auto-selects per layer)")

    # Build
    print("Building INT8 engine (calibrating + compiling)...")
    serialized = builder.build_serialized_network(network, config)
    if serialized is None:
        raise RuntimeError("Build failed")

    with open(ENGINE_PATH, "wb") as f:
        f.write(serialized)

    size_mb = os.path.getsize(ENGINE_PATH) / 1024 / 1024
    print(f"\nEngine saved: {ENGINE_PATH} ({size_mb:.0f}MB)")

    size_mb2 = len(serialized) / 1024 / 1024
    print(f"Done. Engine: {size_mb:.0f}MB. Run benchmark in container with pycuda.")


if __name__ == "__main__":
    main()
